class Main {
	public static void main(String[] args) {
	int month = 12;
	int date = 31;
	
	// 「12月31日」となるように変数と文字列を連結して出力してください
	System.out.println(month + "月" + date + "日");
	
	// 7を2で割った値を出力してください
	System.out.println(7/2); //int型で出力される
	
	// 7.0を2.0で割った値を出力してください
	System.out.println(7.0/2.0); //double型で出力される
	
	// 7を2.0で割った値を出力してください　//double型で出力される
	System.out.println(7/2.0);
	
	}
}
