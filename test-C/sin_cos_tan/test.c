//標準入出力を利用する為のヘッダファイル(ライブラリ)読み込み
#include <stdio.h>
//三角関数など数学系を利用する為のヘッダファイル(ライブラリ)読み込み
//ライブラリ変数にM_PI(π)がいる模様
//コンパイル時にライブラリリンクオプションが必要
#include <math.h>

int main (void){ //main:メイン関数の意味らしい void:空の意味(引数)
	
	printf("%f\n",sin(0));
	//sin関数の返り値はdouble型、引数はラジアン(弧度法)で指定する
	//%fは実数、倍精度実数の%lfでも良いような気がする（処理速度の問題だろうか)
	
	int i ;
	double rad ;
	
	printf("%s","M_PI(%f指定)は、");
	printf("%f\n",M_PI);
	printf("%s","M_PI(%lf指定)は、");
	printf("%lf\n\n",M_PI);
	
	
	printf("----------------------------------------\n");
	printf("|  x | sin(x)   | cos(x)   | tan(x)    |\n");
	printf("----------------------------------------\n");
	for(i=0;i<91;i++){
		rad = i * M_PI / 180;
		//tangent90°は無限になるので条件分岐で処理
		if(i==90){ //角度を180で割った余りが90になる場合で条件付けできるらしいが、わからんのでこれで
			printf("| %2d°| %f | %f |          ∞|\n",i,sin(rad),cos(rad));
		}else{
			printf("| %2d°| %f | %f | %9.6f |\n",i,sin(rad),cos(rad),tan(rad));
			//%2dの2は二桁桁枠指定の2、%9.6fは9桁幅.小数点以下6桁
		}
	}
	printf("----------------------------------------\n");
	
	return(0);
	
}

